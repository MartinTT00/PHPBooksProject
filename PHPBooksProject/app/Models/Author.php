    | FUNCTIONS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | RELATIONS
    |--------------------------------------------------------------------------
    */
    public function genres()
    {
        return $this->belongsToMany(Genre::class, 'author_genres','author_id', 'genre_id');
    }
    
    public function books()
    {
        return $this->hasMany(Book::class);
